/* 
Q)	자기소개하기
	변수: 이름, 나이, 시력, 취미
	위의 변수를 만들어서 자기소개를 출력해보자

	Tip)
	String name = "최현";
	System.out.println("이름: " + name);

	↓ Console ↓
	이름: 최현
	나이: 21
	시력: 1.0
	취미: 러닝

*/

package practice;

public class Prac01 {
	public static void main(String[] args) {
		String name = "최현";
		int age = 21;
		double sight = 1.0;
		String hobby = "러닝";

		System.out.println("이름: " + name);
		System.out.println("나이: " + age);
		System.out.println("시력: " + sight);
		System.out.println("취미: " + hobby);
	}
}












