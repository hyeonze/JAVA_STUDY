package practice02;

public class Printer {
	//메소드 오버로딩(매개변수 int)
	void print(int a) {
		System.out.println(a);
	}
	
	//메소드 오버로딩(매개변수 boolean)
	void print(boolean a) {
		System.out.println(a);
	}
	
	//메소드 오버로딩(매개변수 double)
	void print(double a) {
		System.out.println(a);
	}
	
	//메소드 오버로딩(매개변수 double)
	void print(String a) {
		System.out.println(a);
	}
}
